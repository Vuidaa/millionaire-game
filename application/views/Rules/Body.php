<h1 class='pageheading'> Game rules </h1>

<div class='text-wrap'>
<p>Who Wants to Win a Million Dollars is a quiz game modeled after the TV show Who Wants to be a MillionaireÂ©. All of the questions in the game are general knowledge based but, unlike the TV show, you aren't playing for real money. Sorry!</p>
</br>
<p>
There are 15 questions standing between you and a (pretend!) million dollar pay day! Each question is presented in multiple choice format.
One of the four choices is the correct answer - your job is to pick the right one! The questions become more difficult as you progress through
the game, but the potential (fake!) payoff increases as well! Miss a question and the game is over.
</p>
</br>
<p>
There are two safe levels, one at $1,000 and the other at $32,000. Once reached, you can not win less than that (imaginary!) amount.
</p>
</br>
<p>If you need help, there are three 'life lines' at your disposal. You can use each one only once during the course of the game,
 so use them wisely! The life lines are:</p>
</br>


<p><b>Vote</b> - The contestant asks the studio audience which answer they believe is correct. The results are immediately displayed on the contestant's and host's screens. Audience's answer is correct 70% of the time.</p></br>
<p><b>50/50</b> - The computer will randomly remove two incorrect answers, leaving the correct answer and one incorrect answer.</p></br>
<p><b>Call a friend </b>- Contestant can make a call, and ask his friend`s advice. Most of the time advice is correct.</p></br>
